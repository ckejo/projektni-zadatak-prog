        private string GenerateKey()
        {
            string input = tbInput.Text.Trim();
            string keyword = tbKeyword.Text.Trim();
            StringBuilder nKeyword = new StringBuilder();
            int iN = input.Length;
            int kN = keyword.Length;

            int kNiniN = iN / kN;
            int kNiniNMod = iN % kN;

            // dodaje onoliko kljuca koliko se celih duzinski sadrzi u plaintextu
            for (int i = 0; i < kNiniN; ++i)
            {
                nKeyword.Append(keyword);
            }

            // dodaje preostali deo
            for (int i = 0; i < kNiniNMod; ++i)
            {
                nKeyword.Append(keyword[i]);
            }

            return nKeyword.ToString().ToLower();
        }

        private void bSolve_Click(object sender, EventArgs e)
        {
            string input = tbInput.Text.ToLower().Trim();
            string nKeyword = GenerateKey();
            StringBuilder cipher = new StringBuilder();
            int len = input.Length;

            for (int i = 0; i < len; ++i)
            {
                int vIndex = 0;
                int kIndex = 0;

                for (int v = 0; v < 26; ++v)
                {
                    if (input[i] == tabulaRecta[v, 0])
                    {
                        vIndex = v;
                        break;
                    }
                }

                for (int k = 0; k < 26; ++k)
                {
                    if (nKeyword[i] == tabulaRecta[0, k])
                    {
                        kIndex = k;
                        break;
                    }
                }

                cipher.Append(tabulaRecta[vIndex, kIndex]);
            }

            tbResult.Text = cipher.ToString();
        }
